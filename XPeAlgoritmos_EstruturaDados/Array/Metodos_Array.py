# Program to demonstrate array methods

# Create an array
my_array = [1, 2, 3, 4, 5]
# Print the array
print(my_array)

# Append an element
my_array.append(6)
# Print the array
print(my_array)

# Pop the last element
my_array.pop()
# Print the array
print(my_array)

# Index of an element
index = my_array.index(3)
# Print the index
print(index)

# Reverse the array
my_array.reverse()
# Print the array
print(my_array)

# Sort the array
my_array.sort()
# Print the array
print(my_array)

